def test_existing_filename_preserved() -> None:
    """
    PASS-TO-PASS: File blocks with existing filename are not modified.

    If a filename is already provided, it should be preserved.
    """
    convert_to_openai_data_block = _import_translator()

    file_block = {
        "type": "file",
        "mime_type": "application/pdf",
        "base64": "Zm9vYmFy",
        "filename": "my-document.pdf",
    }

    # No warning should be issued when filename is provided
    with warnings.catch_warnings(record=True) as w:
        warnings.simplefilter("always")
        result = convert_to_openai_data_block(file_block)
        # Should not have any warnings about filename
        filename_warnings = [x for x in w if "filename" in str(x.message)]
        assert (
            len(filename_warnings) == 0
        ), f"Unexpected warning when filename provided: {[str(x.message) for x in filename_warnings]}"

    # Existing filename should be preserved
    file_data = result["file"]
    assert (
        file_data["filename"] == "my-document.pdf"
    ), f"Expected filename 'my-document.pdf', got '{file_data.get('filename')}'"


