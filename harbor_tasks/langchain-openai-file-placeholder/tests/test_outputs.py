def test_file_block_data_structure() -> None:
    """
    FAIL-TO-PASS: Complete file block structure is valid for OpenAI.

    Verifies that the entire output structure matches what OpenAI expects.
    """
    convert_to_openai_data_block = _import_translator()

    file_block = {
        "type": "file",
        "mime_type": "application/pdf",
        "base64": "Zm9vYmFy",
    }

    with warnings.catch_warnings(record=True):
        warnings.simplefilter("always")
        result = convert_to_openai_data_block(file_block)

    # Verify complete structure
    assert isinstance(result, dict), f"Result should be dict, got {type(result)}"
    assert result.get("type") == "file", f"Expected type 'file'"

    file_data = result.get("file", {})
    assert isinstance(file_data, dict), f"file data should be dict"

    # Should have file_data (base64 content)
    assert (
        "file_data" in file_data
    ), f"Expected 'file_data' in file data, got {file_data.keys()}"
    assert file_data["file_data"].startswith(
        "data:application/pdf;base64,"
    ), f"file_data should have data URI prefix"

    # Should now have filename (the fix)
    assert "filename" in file_data, f"Expected 'filename' in file data"
    assert file_data["filename"] == "LC_AUTOGENERATED"
