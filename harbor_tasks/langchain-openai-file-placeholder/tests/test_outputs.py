"""
Test outputs for langchain OpenAI file translator fix.

Tests verify that file blocks without filenames get a default "LC_AUTOGENERATED"
placeholder filename when converted to OpenAI format.
"""

import subprocess
import sys
import warnings
from typing import Any

# Path to the langchain repo
REPO = "/workspace/langchain"
CORE_LIB = f"{REPO}/libs/core"


def _import_translator() -> Any:
    """Import the translator function from the core library."""
    sys.path.insert(0, f"{CORE_LIB}")
    try:
        from langchain_core.messages.block_translators.openai import (
            convert_to_openai_data_block,
        )
        return convert_to_openai_data_block
    finally:
        # Don't remove from path, might need it again
        pass


def test_placeholder_filename_added() -> None:
    """
    FAIL-TO-PASS: File blocks without filename get LC_AUTOGENERATED placeholder.

    Before fix: file block has no "filename" key when not provided
    After fix: file block has "filename": "LC_AUTOGENERATED" when not provided
    """
    convert_to_openai_data_block = _import_translator()

    # File block without filename (the bug scenario)
    file_block = {
        "type": "file",
        "mime_type": "application/pdf",
        "base64": "Zm9vYmFy",  # "foobar" in base64
    }

    # Convert and check result
    with warnings.catch_warnings(record=True):
        warnings.simplefilter("always")
        result = convert_to_openai_data_block(file_block)

    # Verify the result structure
    assert result["type"] == "file", f"Expected type 'file', got {result['type']}"
    assert "file" in result, f"Expected 'file' key in result, got {result.keys()}"

    # THE KEY ASSERTION: filename should be the placeholder
    file_data = result["file"]
    assert (
        "filename" in file_data
    ), f"Expected 'filename' key in file data, got {file_data.keys()}"
    assert (
        file_data["filename"] == "LC_AUTOGENERATED"
    ), f"Expected filename 'LC_AUTOGENERATED', got '{file_data.get('filename')}'"


