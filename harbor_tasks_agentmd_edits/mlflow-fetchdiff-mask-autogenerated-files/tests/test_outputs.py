# [pr_diff] fail_to_pass
def test_autogenerated_shown_with_mask():
    """Auto-generated files (e.g. uv.lock) appear in output with masked content."""
    diff_input = LOCK_FILE_DIFF + REGULAR_FILE_DIFF
    r = _run_filter_diff(
        "import json, sys\n"
        f"diff_input = {json.dumps(diff_input)}\n"
        "result = filter_diff(diff_input)\n"
        "print(json.dumps({'result': result}))\n"
    )
    assert r.returncode == 0, f"Script failed: {r.stderr}"
    data = json.loads(r.stdout.strip())
    result = data["result"]
    # uv.lock must appear in output (not excluded)
    assert "uv.lock" in result, "uv.lock should appear in filtered output"
    # The mask message must be present
    assert "[Auto-generated file - diff masked]" in result, (
        "Auto-generated file should show masked diff message"
    )
    # Actual hunk content should NOT appear for the lock file
    assert "old_lock_content" not in result, "Lock file hunk content should be masked"
    assert "new_lock_content" not in result, "Lock file hunk content should be masked"
    # Regular file content should still appear
    assert "new_code" in result, "Regular file content should be preserved"


