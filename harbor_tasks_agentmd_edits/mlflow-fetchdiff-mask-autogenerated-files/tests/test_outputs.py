def _run_filter_diff(script_body: str, timeout: int = 30) -> subprocess.CompletedProcess:
    """Run a Python script that imports filter_diff with mocked dependencies."""
    setup = (
        "import sys, types\n"
        "sys.path.insert(0, '.claude/skills/src')\n"
        # Pre-register skills.github mock before import so fetch_diff.py
        # doesn't try to import aiohttp/httpx (not installed in test image)
        "import skills\n"
        "github_mod = types.ModuleType('skills.github')\n"
        "github_mod.GitHubClient = None\n"
        "github_mod.parse_pr_url = None\n"
        "sys.modules['skills.github'] = github_mod\n"
        "from skills.commands.fetch_diff import filter_diff\n"
    )
    full_script = setup + script_body
    return subprocess.run(
        [sys.executable, "-c", full_script],
        capture_output=True, text=True, timeout=timeout, cwd=REPO,
    )


