"""Behavioral checks for fizzy-update-agentsmd (markdown_authoring task).

Each test asserts that a distinctive literal from the gold patch appears
in the target tier-1 file after the agent runs.
"""
from __future__ import annotations

from pathlib import Path

REPO = Path("/workspace/fizzy")


def _read(path: str) -> str:
    p = REPO / path
    assert p.is_file(), f"{p} does not exist"
    return p.read_text(encoding="utf-8", errors="replace")


def test_signal_00():
    """Distinctive line from gold patch must be present."""
    text = _read('AGENTS.md')
    assert 'Use Chrome MCP tools to interact with the running dev app for UI testing and debugging.' in text, "expected to find: " + 'Use Chrome MCP tools to interact with the running dev app for UI testing and debugging.'[:80]

